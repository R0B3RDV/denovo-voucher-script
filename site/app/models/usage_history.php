class UsageHistory extends AppModel {
	var $name = 'UsageHistory';
	var $displayField = 'url';
	
	var $belongsTo = array(
		'Site' => array(
			'className' => 'Site',
			'foreignKey' => 'site_id',
			'conditions' => '',
			'fields' => '',
			'order' => ''
		),
		'User' => array(
			'className' => 'User',
			'foreignKey' => 'user_id',
			'conditions' => '',
			'fields' => '',
			'order' => ''
		)
	);	
	
	function insertHistory($site_id, $user_id, $url)
	{
		$this->create();
		$this->save(array(
			'site_id' => $site_id,
			'user_id' => $user_id,
			'url' => $url
		));
	}
}
